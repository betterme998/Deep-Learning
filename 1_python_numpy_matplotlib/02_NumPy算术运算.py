"""
对应元素的乘法
"""
import numpy as np

x = np.array([1.0, 2.0, 3.0])
y = np.array([2.0, 4.0, 6.0])
print(x+y) #对应元素的加法[3. 6. 9.]
print(x-y) #对应元素的减法[-1. -2. -3.]
print(x*y) #对应元素的乘法[ 2.  8. 18.]
print(x/y) #对应元素的除法[0.5 0.5 0.5]

# 广播
# numpy数组和单一数值（标量）运算，需要在numpy的各个元素和标量之间进行运算：被称为广播
print(f'numpy/单一值（标量）{x / 2.0}') # [0.5 1.  1.5] 

